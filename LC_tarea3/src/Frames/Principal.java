    private void sintacticoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sintacticoMouseClicked
        sintax.Analisis();
        String texto="";
        Boolean verificar=Boolean.TRUE;//SI EL BOOL DE MANTIENE EN TRUE NO HAY ERRORES
        for(int i=0;i<sintax.errores.size();i++){
            if(sintax.errores.get(i)==Boolean.FALSE){ //SE ANALIZA SI HAY ERRORES EN DETERMINADAS LINEAS
                texto=texto+"Error en linea "+String.valueOf(i+1)+"\t"+sintax.cad_cand.get(i)+"\n";
                verificar = Boolean.FALSE;
            }
        }
        if(verificar){ //NO HUBO ERRORES
            texto = "Sin errores";
        }
        this.txtAnalizarSintax.setText(texto); //SE MUESTRA EL TEXTO OBTENIDO
        //POR SI SE QUIERE ABRIR OTRO DOCUMENTO
        sintax.resetBool();
        sintax=new sintactico();
        
       analisis_semantico();
    }//GEN-LAST:event_sintacticoMouseClicked

    private void analisis_semantico(){
        int cont = 1;
        String expresion = (String) txtResultado_del_Archivo.getText();

        String aux = "";
        String aux1 ="";
        String resultado = "LINEA " + cont + "\n";

        txtAnalizarSeman.setText(resultado);

        do{
            aux=scan.scanner(expresion + '$');
            if(aux.equals("Salto de linea")){
                cont++;
                resultado += "LINEA " + cont + "\n";
                System.out.println("");
            }
            if (scan.token.getTipo().equals("I")) {
                if((aux.equals("int")||aux.equals("float"))){
                    resultado += "<Tipo Dato>"+"\t"+aux+"\n";
                }else if(aux.equals("escribe")||aux.equals("lee")){
                    resultado += "<Instruccion>"+"\t"+aux+"\n";
                }else{
                    resultado += "<Idetificador>"+"\t"+aux+"\n";
                }
            }
            if(scan.token.getTipo().equals("O")||scan.token.getTipo().equals("OA")){
                if(scan.token.getTipo().equals("O")){
                    resultado += "<Operador>"+"\t"+aux+"\n";   
                }else{
                    resultado += "<Operador A>"+"\t"+aux+"\n";   
                }
            }
            if(scan.token.getTipo().equals("N")||scan.token.getTipo().equals("ND")){
                resultado += "<Numero>"+"\t"+aux+"\n";
            }
            txtAnalizarSeman.setText(resultado);
        }while(!aux.equals("$"));
        scan=new scanner();
        //Muestra el texto en consola
        System.out.println("\n\n===============");
        System.out.println("TEXTO COMPLETO:");
        System.out.println(expresion);
        String salida_consola=expresion;
        
        scanner scan_aux = new scanner();
        int i=0;
        String id;
        do{
            try{
                aux1=scan.scanner(expresion + '$');
                scan_aux.setI(scan.getI());
                //obtener el id que sigue
                id=scan_aux.scanner(expresion+'$');
                if(scan.token.getNom().equals("<<")&&(!scan_aux.token.getTipo().equals("N")||!scan_aux.token.getTipo().equals("ND"))){
                    //obtenemos el indice
                    i=variables_guardar.var.indexOf(scan_aux.token.getNom());

                    salida_consola=salida_consola.replace("escribe<<"+variables_guardar.var.get(i),"escribe<<"+String.valueOf(variables_guardar.val.get(i)));
                    if(salida_consola.contains("escribe<<"+String.valueOf(variables_guardar.val.get(i)))){
                        salida+="escribe<<"+String.valueOf(variables_guardar.val.get(i))+"\n";
                    }
                }
            }catch(Exception e){
                salida+="Variable '"+scan_aux.token.getNom()+"' no declarada\n";
            }
            
        }while(!aux1.equals("$"));
        txtConsola.setText(salida);
        salida="";
        variables_guardar.clear();
    }
    
