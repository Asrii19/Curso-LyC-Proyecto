    private void abrirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_abrirMouseClicked
        JFileChooser chooser=new JFileChooser();
        chooser.showOpenDialog(this);
        File archivo = chooser.getSelectedFile();

        if(archivo!=null){
            this.txtDireccionArchivo.setText(archivo.getAbsolutePath());
        }
        
        String ST;
        try{
            ST=new String(Files.readAllBytes(archivo.toPath()));
            this.txtResultado_del_Archivo.setText(ST);
        }catch(IOException ex){
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }

        String help=archivo.getPath();

        try{
            Scanner scan=new Scanner(new File(help));
            while(scan.hasNextLine()){
                this.sintax.crearCadena(scan.nextLine());
            }
        }catch(FileNotFoundException ex){
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_abrirMouseClicked
